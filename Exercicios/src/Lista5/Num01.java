// Imprima os npumeros que vão de 1 até 10 utilizando o laço de repetição
// -- Enquanto --

package Lista5;

public class Num01 {

	public static void main(String[] args) {
		
		int num = 1;
		int cont = 1;
		
		while(cont <= 10)
		{
			System.out.printf("%s ", num);
			cont++;
			
			num++;
		}

	}

}
