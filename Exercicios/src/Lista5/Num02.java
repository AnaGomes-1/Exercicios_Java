// Imprima os npumeros que vão de 1 até 10 utilizando o laço de repetição
// -- Repita --

package Lista5;

public class Num02 {

	public static void main(String[] args) {
		
		int num = 1;
		
		int cont = 1;
		
		do
		{
			System.out.printf("%s ", num);
			
			num++;
			
			cont++;
		}while(cont <= 10);

	}

}
