package Escola;

public class Aluno {
	// Atributos/Variáveis de Instância da Classe Aluno
	private int matricula = 99999;
	private String nome = "Pedro";
	private String curso = "Tecnico em Informatica";

	// Métodos get (obter dado) e set (alterar dado) da Classe Conta

	public int getMatricula() {
		return matricula;
	}

	public void setMatricula(int matricula) {
		this.matricula = matricula;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCurso() {
		return curso;
	}

	public void setCurso(String curso) {
		this.curso = curso;
	}

}
